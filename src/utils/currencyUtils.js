export function getPriceInTargetCurrency(priceInINR, targetCurrency) {
  const rate = exchangeRates[targetCurrency];
  if (!rate) {
    throw new Error(`Exchange rate for ${targetCurrency} not found!`);
  }
  return priceInINR * rate;
}
